퇴직연금 급여계산 기준 및 산정 방법
=====================================

1. DB형 퇴직급여 계산

1.1 기본 산식
퇴직급여 = 퇴직 전 3개월 평균임금 × 근속연수

1.2 평균임금 산정
- 산정기간: 퇴직일 이전 3개월
- 포함 항목: 기본급, 정기상여, 연차수당, 식대, 교통비
- 제외 항목: 경조사비, 학자금, 실비변상적 급여
- 최저한도: 통상임금 이상

1.3 근속연수 계산
- 기산일: 입사일 (수습기간 포함)
- 종료일: 퇴직일
- 1년 미만 일수: 일할 계산 (해당일수 ÷ 365)

1.4 산정 예시
- 퇴직 전 3개월 임금: 1월 350만원, 2월 380만원, 3월 370만원
- 평균임금: (350 + 380 + 370) ÷ 3 = 366.67만원
- 근속연수: 15년 3개월 = 15.25년
- 퇴직급여: 366.67만원 × 15.25 = 5,591.7만원

2. DC형 퇴직급여 계산

2.1 기본 원리
퇴직급여 = 사용자 부담금 누계 + 운용수익(손실)

2.2 사용자 부담금
- 연간 부담금: 연간 임금총액의 1/12 이상
- 납입주기: 매월 또는 매분기 (근로자-사용자 합의)

2.3 운용수익 반영
- 가입자가 선택한 운용상품의 실적에 따라 변동
- 수익률 = (기말 적립금 - 기초 적립금 - 부담금 납입) ÷ 기초 적립금

2.4 산정 예시
- 연간 임금총액: 5,000만원
- 연간 부담금: 5,000 ÷ 12 = 416.67만원
- 10년 누적 부담금: 약 4,500만원 (임금 인상 반영)
- 연평균 수익률 4% 적용 시 적립금: 약 5,600만원

3. IRP 급여 계산

3.1 일시금 수령
수령액 = 적립금 전액 - 퇴직소득세

3.2 연금 수령
연간 연금수령 한도 = 연금계좌 평가액 ÷ (11 - 연금수령연차) × 120%

3.3 세금 우대
- 연금 수령 시: 퇴직소득세의 60~70% (연금소득세로 분리과세)
- 일시금 수령 시: 퇴직소득세 전액 과세

4. 중간정산/중도인출 시 급여 계산

4.1 DB형 중간정산
- 중간정산 급여: 정산 시점 평균임금 × 정산 대상 근속연수
- 정산 후: 근속연수 리셋 (정산일부터 재계산)

4.2 DC형 중도인출
- 인출 가능 금액: 적립금의 일정 비율 (사유에 따라 50~100%)
- 인출 후: 잔여 적립금 계속 운용

5. 부담금 미납 시 처리

5.1 지연이자
- 미납 기간에 대해 연 10% 지연이자 부과
- 14일 이내 납입 시 지연이자 면제

5.2 시정 명령
- 고용노동부 시정 명령
- 미이행 시 500만원 이하 과태료

6. 퇴직급여 지급기한

6.1 일반 퇴직
- 퇴직일로부터 14일 이내 지급
- 특별한 사정이 있는 경우 당사자 합의로 연장 가능

6.2 사업장 폐쇄/도산
- 임금채권보장기금에서 체불 퇴직급여 지급
- 최대 보장 범위: 퇴직 전 3년간의 퇴직급여

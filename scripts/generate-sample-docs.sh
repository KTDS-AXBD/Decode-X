퇴직연금제도 가입자격 및 운용 안내
===================================

1. 퇴직연금제도 유형

1.1 확정급여형(DB: Defined Benefit)
- 운용 주체: 사용자(회사)
- 급여 수준: 퇴직 시 평균임금 × 근속연수
- 리스크 부담: 사용자
- 적합 대상: 장기 근속 예정자, 안정적 급여 선호자

1.2 확정기여형(DC: Defined Contribution)
- 운용 주체: 가입자(근로자)
- 부담금: 연간 임금총액의 1/12 이상
- 리스크 부담: 가입자
- 적합 대상: 운용 의사결정을 직접 하고 싶은 근로자

1.3 개인형퇴직연금(IRP: Individual Retirement Pension)
- 대상: 퇴직급여를 수령한 자, 자영업자
- 세액공제: 연 700만원 한도 (50세 이상 900만원)
- 의무 가입: 퇴직금 55세 미만 수령자

2. 가입자격 요건

2.1 DB형/DC형 의무 설정
- 상시근로자 1인 이상 사업장
- 가입일: 입사일로부터 1년 경과 후 또는 사업장 퇴직연금 도입일
- 예외: 계속근로기간 1년 미만자, 4주 평균 주 15시간 미만 근로자

2.2 IRP 가입 자격
- 퇴직급여 수급권이 있는 근로자
- 자영업자 (사업소득이 있는 자)
- 퇴직금을 받은 퇴직자 (의무 IRP 이전)

3. 운용 방법 (DC형/IRP)

3.1 원리금보장 상품
- 은행 예금, 보험사 GIC, 국채
- 원금 보장, 안정적 수익
- 수익률: 연 2~4%

3.2 실적배당 상품
- 주식형/혼합형/채권형 펀드
- 원금 비보장, 높은 기대수익
- TDF(Target Date Fund) 활용 권장

3.3 위험자산 비중 제한
- DC형: 위험자산 70% 이하 (2023년 기준)
- IRP: 위험자산 70% 이하
- 위험자산 정의: 주식, 주식형 펀드, 하이일드 채권

4. 급여 지급

4.1 퇴직급여 지급 사유
- 퇴직 (자발적/비자발적)
- 사망
- 제도 폐지

4.2 지급 방식
- 일시금: 전액 일괄 수령
- 연금: 5년 이상 분할 수령 (55세 이후)
- 혼합: 일시금 + 연금 병행

4.3 연금 수령 요건
- 가입기간 10년 이상
- 55세 이상
- 연금수령 한도: (적립금 ÷ 연금수령기간의 11/120) 이내

5. 사업자 의무

5.1 적립금 관리
- 최소 적립비율: 기준책임준비금의 80% 이상 (DB형)
- 적립금 운용 현황 분기별 통보

5.2 교육 의무
- 가입자 교육: 연 1회 이상
- 교육 내용: 제도 운영, 운용방법, 연금수령 절차
